$("#add-append-btn").click(function(){
    $("#add-heading").append("<h1>khoa</h1>")
})

$("#add-prepend-btn").click(function(){
    $("#add-heading").prepend("<h2>khoa</h2>")
})

$("#add-after-btn").click(function(){
    $("#add-heading").after("<h3>khoa</h3>")
})

$("#add-before-btn").click(function(){
    $("#add-heading").before("<h4>khoa</h4>")
})